    // 1. Setor Tunai
    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {                                          
        double masukan = getAmountInput("Masukan jumlah setor tunai :");
        if (masukan > 0) {
            saldo += masukan;
            catatMutasi("Setor Tunai", masukan); // Catat ke history
            updateDisplay();
            JOptionPane.showMessageDialog(rootPane, "Setor Tunai Berhasil!");
        }
    }

    // 2. Tarik Tunai
    private void btnTarikActionPerformed(java.awt.event.ActionEvent evt) {                                         
        double tarik = getAmountInput("Masukan jumlah tarik tunai :");
        if (tarik > 0) {
            if (saldo >= tarik) {
                saldo -= tarik;
                catatMutasi("Tarik Tunai", -tarik); // Catat (minus)
                updateDisplay();
                JOptionPane.showMessageDialog(rootPane, "Penarikan Berhasil.");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Saldo Kurang!", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    // 3. Transfer
    private void btnTransferActionPerformed(java.awt.event.ActionEvent evt) {                                            
        String rekTujuan = JOptionPane.showInputDialog(rootPane, "Masukan Nomor Rekening Tujuan:");
        if (rekTujuan != null && !rekTujuan.isEmpty()) {
            double nominal = getAmountInput("Masukan nominal transfer :");
            if (nominal > 0) {
                if (saldo >= nominal) {
                    saldo -= nominal;
                    catatMutasi("Transfer ke " + rekTujuan, -nominal);
                    updateDisplay();
                    JOptionPane.showMessageDialog(rootPane, "Transfer Berhasil!");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Saldo Kurang!", "Gagal", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }

    // 4. TOP UP E-WALLET (NEW FEATURE)
    private void btnTopUpActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // Pilihan E-Wallet
        String[] options = {"GOPAY", "DANA", "OVO", "SHOPEEPAY"};
        int choice = JOptionPane.showOptionDialog(rootPane, "Pilih E-Wallet:", "Top Up",
                JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
        
        if (choice >= 0) { // Jika user memilih salah satu
            String walletName = options[choice];
            String nomorHP = JOptionPane.showInputDialog("Masukkan Nomor HP " + walletName + ":");
            
            if (nomorHP != null && !nomorHP.isEmpty()) {
                double nominal = getAmountInput("Jumlah Top Up:");
                if (nominal > 0) {
                    if (saldo >= nominal) {
                        saldo -= nominal;
                        catatMutasi("Top Up " + walletName, -nominal);
                        updateDisplay();
                        JOptionPane.showMessageDialog(rootPane, "Top Up " + walletName + " Berhasil!");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Saldo Tidak Cukup!");
                    }
                }
            }
        }
    }

    // 5. LIHAT MUTASI (NEW FEATURE)
    private void btnMutasiActionPerformed(java.awt.event.ActionEvent evt) {                                          
        StringBuilder sb = new StringBuilder();
        sb.append("=== RIWAYAT TRANSAKSI ===\n\n");
        if (mutasiRekening.isEmpty()) {
            sb.append("(Belum ada transaksi)");
        } else {
            // Loop untuk mengambil data dari ArrayList
            for (String s : mutasiRekening) {
                sb.append(s).append("\n");
            }
        }
        JOptionPane.showMessageDialog(rootPane, sb.toString(), "Mutasi Rekening", JOptionPane.INFORMATION_MESSAGE);
    }

    // 6. CEK KURS DOLLAR (NEW FEATURE)
    private void btnKursActionPerformed(java.awt.event.ActionEvent evt) {                                        
        double kursUSD = 16250; // Anggap 1 USD = Rp 16.250
        double nilaiUSD = saldo / kursUSD;
        
        String pesan = String.format("Saldo Anda: Rp %,.2f\n\nEstimasi dalam USD:\n$ %,.2f", saldo, nilaiUSD);
        JOptionPane.showMessageDialog(rootPane, pesan, "Konversi Mata Uang", JOptionPane.INFORMATION_MESSAGE);
    }

    // 7. Logout - NEW FEATURE
