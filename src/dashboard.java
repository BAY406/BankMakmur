    // Helper method untuk meminta input angka yang aman
    private double getAmountInput(String message) {
        String input = JOptionPane.showInputDialog(rootPane, message);
        if (input == null || input.isEmpty()) return 0;
        try {
            return Double.parseDouble(input);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Input harus angka!", "Error", JOptionPane.ERROR_MESSAGE);
            return 0;
        }
    }

